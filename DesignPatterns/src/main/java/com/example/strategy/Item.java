package com.example.strategy;

public class Item {
	int itemCode;
	int cost;
	public Item(int itemCode, int cost) {
		super();
		this.itemCode = itemCode;
		this.cost = cost;
	}
	public int getItemCode() {
		return itemCode;
	}
	public void setItemCode(int itemCode) {
		this.itemCode = itemCode;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	
	
	

}
