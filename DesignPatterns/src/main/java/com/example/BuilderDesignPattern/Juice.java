package com.example.BuilderDesignPattern;

public class Juice {
	 private int water;
	    private int fruit;
	    private int sugar;
	    private String juicename;
	    private String juceType;
		public int getWater() {
			return water;
		}
		public void setWater(int water) {
			this.water = water;
		}
		public int getFruit() {
			return fruit;
		}
		public void setFruit(int fruit) {
			this.fruit = fruit;
		}
		public int getSugar() {
			return sugar;
		}
		public void setSugar(int sugar) {
			this.sugar = sugar;
		}
		public String getJuicename() {
			return juicename;
		}
		public void setJuicename(String juicename) {
			this.juicename = juicename;
		}
		public String getJuceType() {
			return juceType;
		}
		public void setJuceType(String juceType) {
			this.juceType = juceType;
		}
		
	    
	    
}
